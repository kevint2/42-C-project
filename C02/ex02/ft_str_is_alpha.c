int	ft_str_is_alpha(char *str)
{
	int	num ;

	num = 0;
	while (str[num] != '\0')
	{
		if ((str[num] < 'a' || str[num] > 'z')
			&& (str[num] < 'A' || str[num] > 'Z'))
		{
			return (0);
		}
		num++;
	}
	return (1);
}
